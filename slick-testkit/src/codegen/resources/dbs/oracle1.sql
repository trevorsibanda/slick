CREATE TABLE "PERSON"(
  "ID" NUMBER(11,0) NOT NULL ENABLE,
  "PERSON_TYPE" CHAR(1 BYTE) DEFAULT 'Y' NOT NULL ENABLE,
  "AGE" NUMBER DEFAULT 0 NOT NULL ENABLE,
  CONSTRAINT "PERSON_PK" PRIMARY KEY ("ID")
);

CREATE TABLE AUTH_PERMISSION
  (
      ID NUMBER(19,0) GENERATED BY DEFAULT ON NULL AS IDENTITY ALWAYS,
     -- ID NUMBER(19,0) PRIMARY KEY NOT NULL,
      NAME VARCHAR2(50) UNIQUE NOT NULL,
      ACTION_ID NUMBER(19,0) NOT NULL,
      RESOURCE_ID NUMBER(19,0) NOT NULL,
      ENVIRONMENT_ID NUMBER(19,0) NOT NULL
  );